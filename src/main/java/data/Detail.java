package data;

public class Detail {
	
	int id;
	int status;
	String userid;
	String moimid;
	
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getMoimid() {
		return moimid;
	}
	public void setMoimid(String moimid) {
		this.moimid = moimid;
	}

}
